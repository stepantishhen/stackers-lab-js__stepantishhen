// 1. Вам дан массив чисел. Создайте функцию,
// которая увеличит каждое число на его позицию в массиве.
// Результат может содержать только однозначные числа,
// так что если добавление цифры с её позицией дает
// вам многозначное число, то должна быть возвращена только последняя цифра.
// (массивы будут содержать только числа)
//
// Пример:
//     [1, 2, 4]  -->  [2, 4, 7]
// #[1+1, 2+2, 4+3]
//
//     [4, 6, 9, 1, 3]  -->  [5, 8, 2, 5, 8]
// # [4+1, 6+2, 9+3, 1+4, 3+5]
// # 9+3 = 12  -->  2
function increaseNumbers(arr) {
    return arr.map((num, index) => {
        let newNum = num + index + 1;
        return String(newNum).slice(-1);
    });
}

// Test cases
console.log(increaseNumbers([1, 2, 4])); // Output: [2, 4, 7]
console.log(increaseNumbers([4, 6, 9, 1, 3])); // Output: [5, 8, 2, 5, 8]